package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); // 5는 3보다 크기에 true (boolean 형)
        System.out.println(5 >= 3); // 5는 3보다 크거나 같기 때문에 true
        System.out.println(5 >= 5); // true
        System.out.println(5 >= 7); // false

        System.out.println(5 < 3); // f

        System.out.println(5 == 5); // t
        System.out.println(5 != 7); // 5와 7은 같지 않기에 true
    }
}
